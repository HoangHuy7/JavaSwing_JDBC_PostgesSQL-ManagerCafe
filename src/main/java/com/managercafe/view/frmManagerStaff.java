    private void displayTable(List<userModel> listUser) {

        String[] header = {"ID", "Full Name", "User Name", "Password", "Role", "Status", "Created Date", "Create By", "Modified Date", "Modifiel By"};
        DefaultTableModel headerModel = new DefaultTableModel(header, 0);
        tblStaff.setModel(headerModel);
        Vector row = null;
        DefaultTableModel model = (DefaultTableModel) tblStaff.getModel();
        for (userModel usr : listUser) {
            row = new Vector();
            row.add(usr.getId());
            row.add(usr.getFullname());
            row.add(usr.getUserName());
            row.add(usr.getPassword());
            row.add(chckUtil.checkRole(usr.getRoleid().toString()));
            row.add(chckUtil.checkStatus(usr.getStatus()) );
            row.add(usr.getCreateddate());
            row.add(usr.getCreatedby());
            row.add(usr.getModifieddate());
            row.add(usr.getModifiedby());
            model.addRow(row);
        }
        tblStaff.getColumnModel().getColumn(0).setPreferredWidth(40);
        tblStaff.getColumnModel().getColumn(1).setPreferredWidth(150);
        tblStaff.getColumnModel().getColumn(2).setPreferredWidth(100);
        tblStaff.getColumnModel().getColumn(3).setPreferredWidth(50);
        tblStaff.getColumnModel().getColumn(4).setPreferredWidth(40);
        tblStaff.getColumnModel().getColumn(5).setPreferredWidth(30);
        tblStaff.getColumnModel().getColumn(6).setPreferredWidth(150);
        tblStaff.getColumnModel().getColumn(7).setPreferredWidth(40);
        tblStaff.getColumnModel().getColumn(8).setPreferredWidth(150);
        tblStaff.getColumnModel().getColumn(9).setPreferredWidth(40);
        tblStaff.setModel(model);
    }
