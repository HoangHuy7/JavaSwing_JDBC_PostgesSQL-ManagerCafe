    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        int yesNo = notiUTIL.QuestionYesNo(this, "Do you want insert this ?");
        // Because 0 is choice Yes , 2 is choice No
        if (yesNo == 0) {
            if (userService.insertUser(setUser(new userModel()))) {
                displayTable(userService.findStatusOn());
                notiUTIL.NotiSuccess(this, "Insert successfully !");
            } else {
                notiUTIL.NotiError(this, "Insert error !");
            }
        }

    }//GEN-LAST:event_btnAddActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        int yesNo = notiUTIL.QuestionYesNo(this, "Do you want delete this ?");
        // Because 0 is choice Yes , 2 is choice No
        if (yesNo == 0) {
            String id = tblStaff.getModel().getValueAt(tblStaff.getSelectedRow(), 0).toString(); // get id in table
            if (userService.setStatus(Long.parseLong(id) , 0)) {
                displayTable(userService.findStatusOn());
                notiUTIL.NotiSuccess(this, "Delete successfully !");
            } else {
                notiUTIL.NotiError(this, "Delete error !");
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnRepairActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRepairActionPerformed
        int yesNo = notiUTIL.QuestionYesNo(this, "Do you want repair this ?");
        // Because 0 is choice Yes , 2 is choice No
        if (yesNo == 0) {
            String id = tblStaff.getModel().getValueAt(tblStaff.getSelectedRow(), 0).toString(); // get id in table
            userModel usr = new userModel();
            usr.setId(Long.parseLong(id));  // set id because function setUser don't set id
            if (userService.repair(setUser(usr))) {
                displayTable(userService.findStatusOn());
                notiUTIL.NotiSuccess(this, "Repair successfully !");
            } else {
                notiUTIL.NotiError(this, "Repair error !");
            }
        }
    }//GEN-LAST:event_btnRepairActionPerformed

