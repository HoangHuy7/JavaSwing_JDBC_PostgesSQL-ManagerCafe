    private userModel setUser(userModel usr) {
        usr.setUserName(txtUserName.getText());
        usr.setPassword(new String(txtPassword.getPassword()));
        usr.setFullname(txtFullName.getText());
        String roleId = cbbRole.getSelectedItem().toString();
        if (null != roleId) {
            switch (roleId) {
                case "Staff":
                    usr.setRoleid(3L);
                    break;
                case "Manager":
                    usr.setRoleid(2L);
                    break;
                case "Admin":
                    usr.setRoleid(1L);
                    break;
                default:
                    break;
            }
        }
        String status = cbbStatus.getSelectedItem().toString();
        if ("Work".equals(status)) {
            usr.setStatus(1);
        } else if ("Leave".equals(status)) {
            usr.setStatus(0);
        }
        return usr;
    }
    
