    private void btnRepairActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRepairActionPerformed
        int yesNo = notiUTIL.QuestionYesNo(this, "Do you want repair this ?");
        // Because 0 is choice Yes , 2 is choice No
        if (yesNo == 0) {
           productModel pd = new productModel();
           pd.setId(Long.parseLong( tblProduct.getModel().getValueAt(tblProduct.getSelectedRow(), 0).toString()));
            if (productService.update(setProduct(pd))) {
                displayTable(productService.findAll());
                notiUTIL.NotiSuccess(this, "Upadate successfully !");
            } else {
                notiUTIL.NotiError(this, "Upadate error !");
            }
        }
    }//GEN-LAST:event_btnRepairActionPerformed

