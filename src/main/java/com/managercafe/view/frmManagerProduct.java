    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        boolean f = false;
        if (cbbType.getItemCount() > 0) {
            String[] field = {txtName.getText(), txtPrice.getText()};
            f = check.checkAll(field);
        }
        
        if (f) {
            int yesNo = notiUTIL.QuestionYesNo(this, "Do you want insert this ?");
            // Because 0 is choice Yes , 2 is choice No
            if (yesNo == 0) {
                if (productService.insert(setProduct(new productModel()))) {
                    displayTable(productService.findAll());
                    notiUTIL.NotiSuccess(this, "Insert successfully !");
                } else {
                    notiUTIL.NotiError(this, "Insert error !");
                }
            }
        } else {
            notiUTIL.NotiError(this, "Pls type all field !");
        }


    }//GEN-LAST:event_btnAddActionPerformed

