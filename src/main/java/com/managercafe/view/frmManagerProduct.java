    private void displayTable(List<productModel> listProduct) {

        String[] header = {"ID", "Name", "Price", "Size", "Type", "Created Date", "Create By", "Modified Date", "Modifiel By"};
        DefaultTableModel headerModel = new DefaultTableModel(header, 0);
        tblProduct.setModel(headerModel);
        Vector row = null;
        DefaultTableModel model = (DefaultTableModel) tblProduct.getModel();
        for (productModel product : listProduct) {
            row = new Vector();
            row.add(product.getId());
            row.add(product.getName());
            String price = product.getPrice().toString();
            row.add(check.covertPrice(price));
            if (product.getSize() == 1) {
                row.add("L");
            } else if (product.getSize() == 2) {
                row.add("S");
            }

            row.add(product.getType());
            row.add(product.getCreateddate());
            row.add(product.getCreatedby());
            row.add(product.getModifieddate());
            row.add(product.getModifiedby());
            model.addRow(row);
        }

        tblProduct.setModel(model);
    }

    private productModel setProduct(productModel product) {
        product.setName(txtName.getText());
        product.setPrice(Long.parseLong(check.covertPriceTable(txtPrice.getText())));
        String size = cbbSize.getSelectedItem().toString();
        if (null != size) {
            switch (size) {

                case "L":
                    product.setSize(1);
                    break;
                case "S":
                    product.setSize(2);
                    break;
                default:
                    break;
            }
        }
        product.setType(cbbType.getSelectedItem().toString());
        return product;
    }

    private void LoadType() {
        cbbType.removeAllItems();
        typeService.findAll().forEach(productModel -> {
            cbbType.addItem(productModel.getName());
        });
    }

