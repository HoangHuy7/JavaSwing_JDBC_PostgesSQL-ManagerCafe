    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        int yesNo = notiUTIL.QuestionYesNo(this, "Do you want insert this ?");
        // Because 0 is choice Yes , 2 is choice No
        if (yesNo == 0) {
            if (typeService.insert(setType(new typeProductModel()))) {
                displayTable(typeService.findAll());
                notiUTIL.NotiSuccess(this, "Insert successfully !");
            } else {
                notiUTIL.NotiError(this, "Insert error !");
            }
        }
    }//GEN-LAST:event_btnAddActionPerformed

