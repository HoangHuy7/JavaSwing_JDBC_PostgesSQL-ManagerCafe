    private void displayTable(List<typeProductModel> listType) {

        String[] header = {"Name","Created Date", "Create By", "Modified Date", "Modifiel By"};
        DefaultTableModel headerModel = new DefaultTableModel(header, 0);
        tblType.setModel(headerModel);
        Vector row = null;
        DefaultTableModel model = (DefaultTableModel) tblType.getModel();
        for (typeProductModel type : listType) {
            row = new Vector();
            row.add(type.getName());
            row.add(type.getCreateddate());
            row.add(type.getCreatedby());
            row.add(type.getModifieddate());
            row.add(type.getModifiedby());
            model.addRow(row);
        }

        tblType.setModel(model);
    }
    private typeProductModel setType(typeProductModel type){
        type.setName(txtName.getText());
        return type;
    }
